default_SOL_CRK = 0.50
"""per layer - Potential or maximum crack volume of the soil profile optional

Returns:
    float -- default_SOL_CRK
"""


def swat_ext_defaults_lookup(texture_code: str) -> Dict[str, float]:
    """a lookup function that provides dict based access to some default values that were sourced from some original data and averaged

    Arguments:
        texture_code {str} -- [description]

    Returns:
        Dict[str, float] -- [description]
    """

    # S Sands and LS Loamy Sand
    if texture_code in ['l', 'l1', 'l2', 'l3', 'l4', 'l5', 'pl', 'dk']:
        # SOL_BD: l     1.159737 -> 1,65	(1,55-1,80)
        # SOL_AWC: l     0.221842
        # SOL_CBN: l     10.147368
        # SOL_K1: l     170.102632
        # SOL_ALB: l     0.113158
        return {'SOL_BD': 1.65, 'SOL_AWC': 0.15, 'SOL_CBN': 10.147368, 'SOL_K': 170.102632, 'SOL_ALB': 0.1}

    # SL Sandy Loam
    elif texture_code in ['plsl', 'sl', 'sl1', 'sl2', 'sl3', 'sl4', 'tsl', 'tsl1', 'ls1']:
        # SOL_BD: sl    1.211000 -> 1,50	(1,40-1,60)
        # SOL_AWC: sl    0.216200
        # SOL_CBN: sl     6.895000
        # SOL_K1: sl     86.255000
        # SOL_ALB: sl    0.115500
        return {'SOL_BD': 1.5, 'SOL_AWC': 0.20, 'SOL_CBN': 6.895000, 'SOL_K': 86.255000, 'SOL_ALB': 0.11}

    # L Loam
    elif texture_code in ['ls', 'ls2', 'tls1']:
        # SOL_BD: ls    1.278500 -> 1,40	(1,35-1,60)
        # SOL_AWC: ls    0.207375
        # SOL_CBN: ls     7.052500
        # SOL_K1: ls     63.967500
        # SOL_ALB: ls    0.114500
        return {'SOL_BD': 1.4, 'SOL_AWC': 0.316, 'SOL_CBN': 7.052500, 'SOL_K': 63.967500, 'SOL_ALB': 0.11}

    # CL Clay Loam
    elif texture_code in ['ls3', 'ls4']:
        # SOL_BD: ls    1.278500 -> 1,35	(1,35-1,40)
        # SOL_AWC: ls    0.207375
        # SOL_CBN: ls     7.052500
        # SOL_K1: ls     63.967500
        # SOL_ALB: ls    0.114500
        return {'SOL_BD': 1.35, 'SOL_AWC': 0.183, 'SOL_CBN': 7.052500, 'SOL_K': 63.967500, 'SOL_ALB': 0.11}

    # CL Silty Loam and Silty Clay Loam
    elif texture_code in ['tls', 'tls2', 'tls3']:
        # SOL_BD: ls    1.278500 -> 1,35	(1,35-1,40)
        # SOL_AWC: ls    0.207375
        # SOL_CBN: ls     7.052500
        # SOL_K1: ls     63.967500
        # SOL_ALB: ls    0.114500
        return {'SOL_BD': 1.35, 'SOL_AWC': 0.208, 'SOL_CBN': 7.052500, 'SOL_K': 63.967500, 'SOL_ALB': 0.11}

    # CLAY
    elif texture_code in ['s', 's1', 's2']:
        # SOL_BD: s     1.199474  -> 1,25	(1,25-1,30)
        # SOL_AWC: s     0.247789
        # SOL_CBN: s      8.263158
        # SOL_K1: s      35.884211
        # SOL_ALB: s     0.111579
        return {'SOL_BD': 1.25, 'SOL_AWC': 0.2, 'SOL_CBN': 8.263158, 'SOL_K': 35.884211, 'SOL_ALB': 0.11}

    # HUMUS
    elif texture_code in ['th']:
        # SOL_BD: st    0.080000,1.500000,1.470000
        # SOL_AWC: st    0.250000,0.120000,0.200000
        # SOL_CBN: st    27.000000,tl     0.000000,tt     1.300000
        # SOL_K1: st    170.000000,tl    158.000000,tt     32.000000
        # SOL_ALB:  st    0.120000,tl    0.120000,tt    0.120000
        return {'SOL_BD': 1.47, 'SOL_AWC': 0.4, 'SOL_CBN': 27.000000, 'SOL_K': 158.000000, 'SOL_ALB': 0.12}

    # PEAT
    elif texture_code in ['t', 't1', 't2', 't3']:
        # SOL_BD: t     0.890000
        # SOL_AWC: t     0.225778
        # SOL_CBN: t     14.248148
        # SOL_K1: t      64.996296  # literature override of Rosetta
        # SOL_ALB: t     0.120000
        return {'SOL_BD': 0.89, 'SOL_AWC': 0.5, 'SOL_CBN': 14.248148, 'SOL_K': 64.996296, 'SOL_ALB': 0.12}

    # GRAVELS
    else:
        return {'SOL_BD': default_SOL_BD,
                'SOL_AWC': default_SOL_AWC,
                'SOL_CBN': default_SOL_CBN,
                'SOL_K': default_SOL_K,
                'SOL_ALB': default_SOL_ALB
                }


